let CAPS = document.querySelector("#CAPS");
CAPS.addEventListener('mouseover', function(){
    CAPS.style.boxShadow='0px 0px 15px 0px rgba(174, 174, 174, 1)';
})
CAPS.addEventListener('mouseout', function(){
    CAPS.style.boxShadow='0px 0px 8px 0px rgba(227, 227, 227, 1)';
})
CAPS.addEventListener('touchstart', function(){
    CAPS.style.boxShadow='0px 0px 15px 0px rgba(174, 174, 174, 1)';
})
CAPS.addEventListener('touchend', function(){
    CAPS.style.boxShadow='0px 0px 8px 0px rgba(227, 227, 227, 1)';
})
CAPS.addEventListener('click', function(){
    CAPS.style.boxShadow='0px 0px 8px 0px rgba(227, 227, 227, 1)';
})


